import sys

S = sys.stdin.readline().strip()

# 목표 순서: ( -> 1 -> )
# S에서 ( -> 1 -> ) 순서를 따르는 부분 수열의 최대 길이를 구합니다.

def solve():
    if S == "(1)":
        return 0
    
    # 순서가 맞는 문자가 2개인 경우 (예: (가 1보다 앞에 있거나, 1이 )보다 앞에 있거나 등)
    # ( 가 1보다 앞에 있는 경우: (1) , ( ) 1 , 1 ( ) 등에서 (와 1의 상대 위치
    # 하지만 여기서는 '연속할 필요 없는' 순서만 보면 됩니다.
    
    # 1) '(' 가 '1' 보다 앞에 있는가?
    # 2) '1' 이 ')' 보다 앞에 있는가?
    # 3) '(' 가 ')' 보다 앞에 있는가?
    
    # 만약 위 조건 중 하나라도 맞으면, 나머지 하나만 옮기면 되므로 1번입니다.
    # 단, ( ) 1 처럼 1번과 3번이 동시에 맞아도 1번만 옮기면 됩니다.
    
    pos_open = S.find('(')
    pos_one = S.find('1')
    pos_close = S.find(')')
    
    # ( 가 1보다 앞에 있거나 AND 1이 )보다 앞에 있으면 이미 (1)임 (0번)
    if pos_open < pos_one < pos_close:
        return 0
    
    # 두 글자의 순서라도 맞으면 (예: ( 가 1보다 앞에 있음) 
    # 나머지 한 글자만 제자리로 쏙 끼워넣으면 되므로 1번입니다.
    if pos_open < pos_one or pos_one < pos_close or pos_open < pos_close:
        return 1
    
    # 그 외의 경우 (완전히 역순인 )1( 등)은 2번입니다.
    return 2

print(solve())