# 사용자로부터 k를 입력받습니다.
k_input = input()

# 입력값이 비어있지 않은지 확인 후 정수로 변환합니다.
if k_input:
    k = int(k_input)

    # 1. 2^k - 1 은 이진수로 '1'이 k개인 수입니다.
    # 2. 여기에 2^(k-1)을 곱하는 것은 뒤에 '0'을 k-1개 붙이는 것과 같습니다.
    # 예: k=3 -> '111' + '00' = '11100'
    
    ones = '1' * k
    zeros = '0' * (k - 1)
    
    print(ones + zeros)